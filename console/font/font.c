void tobigchar(int big[2], int n)
{
    for (int i = 0; i < 8; i++)
    {
        for (int j = 0; j < 8; j++)
        {
            bc_setbigcharpos(big, j, i, (int)bigchars[n][(8 * i) + j]);
        }
    }
}

int main()
{
    int big[18][2];

    // mt_clrscr();
    for (int i = 0; i < 18; i++)
    {
        tobigchar(big[i], i);
    }

    int fd = open("font.bin", O_CREAT | O_WRONLY | O_TRUNC, S_IRWXU);

    bc_bigcharwrite(fd, *big, 18);

    close(fd);
}